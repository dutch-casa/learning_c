#include <stdio.h>
#include <stdlib.h>

int main() {
    // Format specifiers
    
    printf("My favorite %s is %d\n", "number", 12);
    /*
    * %d - integer (same as %i)
    * %i - integer
    * %s - string
    * %f - float
    * %c - character
    * %u - unsigned integer
    * %x - hex
    * %o - octal
    */
}